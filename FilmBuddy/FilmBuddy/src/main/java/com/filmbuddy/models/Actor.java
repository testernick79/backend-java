package com.filmbuddy.models;

public class Actor {
	
	
	private String actor;
    private String film;
    
    
    public String getActor() {
        return actor;
    }
    
    public void setActor(String actor) {
        this.actor = actor;
    }

	public String getFilm() {
		return film;
	}

	public void setFilm(String film) {
		this.film = film;
	}
 
}